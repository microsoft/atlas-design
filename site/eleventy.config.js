const fs = require('fs');
const path = require('path');
const { renderPage, parseFrontmatter } = require('./lib/render-page');
const { rewriteHtml } = require('./lib/url-rewrite');

const INPUT_DIR = 'src';
const OUTPUT_DIR = 'dist';

module.exports = function (eleventyConfig) {
	// Only markdown files are treated as templates. Everything else in src/
	// (scaffold html/ts/scss, media, svg) is either ignored or passed through.
	eleventyConfig.setTemplateFormats(['md']);

	// Static assets.
	eleventyConfig.addPassthroughCopy({ 'src/atlas-light.svg': 'atlas-light.svg' });
	eleventyConfig.addPassthroughCopy({ 'src/scaffold/media': 'scaffold/media' });

	// The compiled CSS/JS (produced by build-assets.mjs) lives in the output
	// folder. Let the dev server watch them directly so a CSS/JS recompile
	// triggers a fast browser reload without a full Eleventy rebuild — this is
	// the primary CSS-development loop.
	eleventyConfig.setServerOptions({
		port: 1111,
		watch: [
			'dist/scaffold/index.css',
			'dist/scaffold/styles/homepage.css',
			'dist/scaffold/index.js'
		]
	});

	// These require an actual rebuild (they change rendered HTML), so use
	// watch targets that re-run the page renderer.
	eleventyConfig.addWatchTarget('src/scaffold/toc.json');
	eleventyConfig.addWatchTarget('src/scaffold/standard.html');
	eleventyConfig.addWatchTarget('src/scaffold/homepage.html');
	eleventyConfig.addWatchTarget('src/scaffold/token.html');
	// Shared render logic — rebuild when the renderer or its helpers change.
	eleventyConfig.addWatchTarget('lib/');

	const inputAbs = path.join(process.cwd(), INPUT_DIR);
	const distAbs = path.join(process.cwd(), OUTPUT_DIR);

	// Table of contents, generated by toc.js into src/scaffold/toc.json before
	// the build. Registered programmatically (rather than as a src/_data file)
	// so the data directory doesn't appear inside the documented src/ tree.
	eleventyConfig.addGlobalData('toc', () => {
		const tocPath = path.join(process.cwd(), 'src', 'scaffold', 'toc.json');
		return JSON.parse(fs.readFileSync(tocPath, 'utf-8'));
	});

	const srcRelative = inputPath => path.relative(inputAbs, inputPath).split(path.sep).join('/');

	// Custom markdown handling that mirrors the Parcel markdown transformer.
	eleventyConfig.addExtension('md', {
		read: true,
		// Output `<name>.html` to match the previous Parcel URLs exactly.
		getData: async inputPath => {
			const raw = fs.readFileSync(inputPath, 'utf-8');
			const { attributes } = parseFrontmatter(raw);
			return {
				...attributes,
				permalink: srcRelative(inputPath).replace(/\.md$/, '.html')
			};
		},
		compile: (inputContent, inputPath) => {
			return async data => {
				const raw = fs.readFileSync(inputPath, 'utf-8');
				return renderPage({
					rawContent: raw,
					srcRelativePath: srcRelative(inputPath),
					toc: data.toc
				});
			};
		}
	});

	// Resolve tilde paths, rewrite .md links to .html, and relativize all
	// root-absolute URLs so the site works from a subpath or a domain root.
	eleventyConfig.addTransform('atlas-urls', function (content) {
		const outputPath = this.page && this.page.outputPath;
		if (!outputPath || !outputPath.endsWith('.html')) {
			return content;
		}
		return rewriteHtml(content, outputPath, distAbs);
	});

	return {
		dir: { input: INPUT_DIR, output: OUTPUT_DIR }
	};
};
